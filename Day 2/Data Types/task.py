# Strings are characters strung together with double or single quotes around.
print("Hello")

# Subscripting: Get the nth letter from string enclosed in square brackets.
print("Hello"[0])

# Get the last letter 'o' from the string 'Hello'
print("Hello"[4]) # Positive indexing
print("Hello"[-1]) # Negative indexing

# Even the numbers stored inside the double or single quotes are considered as string and calculations are not performed on them
print("123" + "456")


# Integer = Whole number
print(123 + 456)

# Large integers
print(123,456,789)

# Float = Floating point number (with decimal)
print(3.1459)

# Boolean: Have only True and False values.
print(True)
print(False)

